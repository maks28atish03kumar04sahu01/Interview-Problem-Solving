// 9. ARITHMETIC OPERATIONS IN PROGRAMMING
const func = (ip1: number, ip2: number): void => {
    console.log(`${ip1} + ${ip2} = ${ip1 + ip2}`);
    console.log(`${ip1} - ${ip2} = ${ip1 - ip2}`);
    console.log(`${ip1} * ${ip2} = ${ip1 * ip2}`);
    console.log(`${ip1} / ${ip2} = ${ip1 / ip2}`);
    console.log(`${ip1} % ${ip2} = ${ip1 % ip2}`);
    console.log();
}
func(124, 7);
func(358, 33);